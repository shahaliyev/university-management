public class School {
    String schoolMajor;
    String location;
    int tuitionFees;
    boolean isActive;

    public School(String schoolMajor, String location, int tuitionFees, boolean isActive) {
        this.schoolMajor = schoolMajor;
        this.location = location;
        this.tuitionFees = tuitionFees;
        this.isActive = isActive;
    }
}